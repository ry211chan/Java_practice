public class ChangeValue2 {

	public static void main(String[] args) {
		// TODO 自動生成されたメソッド・スタブ
		//数値を2つ変数に⽤意
		int num1 = 7;
		int num2 = 4;
		
		System.out.print("num1 = ");
		System.out.println(num1);
		System.out.print("num2 = ");
		System.out.println(num2);
		
		System.out.println("*********");
		
		//num2 に num1 の値を代入
		num2 = num1;
		
		System.out.print("num1 = ");
		System.out.println(num1);
		System.out.print("num2 = ");
		System.out.println(num2);

	}

}
